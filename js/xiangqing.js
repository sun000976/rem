$(function () {
    var xq=getUrlParams("news");
    var tuputitle=xiangqing[xq].tuputitle;
    var tupuqy=xiangqing[xq].tupuqy;
    var data=xiangqing[xq].data;
    var list=data.list;
    
    $(".content-box-nei>h1").html(tuputitle);
    $(".qianyan .tupuqy").html(tupuqy);
    for (var i = 0; i < list.length; i++) {
        var htmlModel = $('#moban').html()
        htmlModel = htmlModel.replace("img/5c00e8d3d6f6b.png", list[i].img)
        htmlModel = htmlModel.replace("$title$", list[i].title)
        $('.content-box-nei-list').append(htmlModel)
    }

        function getUrlParams(name) {
            //获取页面url传过来的参数    active.html?news=xiaoniaoNews1
            var reg = new RegExp("(^|&)" + name + "=([^|&]*)(&|$)")
            var r=window.location.search.substring(1).match(reg)
            //news=xiaoniaoNews1
            if(r!=null){
                return r[2]

            }else{
                return ""
            }
        }

        $(window).scroll(function(){
            if($(window).scrollTop()>1000){
                $(".totop").show()
            }else{
                $(".totop").hide()
            }
        })
        $(".totop").click(function(){
            $("html,body").animate({"scrollTop":0})
        })
 
})


